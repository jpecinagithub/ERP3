const TRANSLATABLE_ATTRIBUTES = ['placeholder', 'title', 'aria-label'];
const SKIP_TAGS = new Set(['SCRIPT', 'STYLE', 'NOSCRIPT', 'TEXTAREA', 'CODE', 'PRE']);

const escapeRegExp = (value) => value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');

const isCapitalized = (text) => text.length > 0 && text[0] === text[0].toUpperCase();

const applyCase = (source, translated) => {
  if (!translated) return translated;
  if (source.toUpperCase() === source) return translated.toUpperCase();
  if (isCapitalized(source)) return translated.charAt(0).toUpperCase() + translated.slice(1);
  return translated;
};

const applyRegexRules = (text) => {
  let result = text;

  result = result.replace(/^Asientos\s+Contables$/i, 'Journal Entries');
  result = result.replace(/^Journal Entries\s+Contables$/i, 'Journal Entries');
  result = result.replace(/^Bienvenido de nuevo,\s*(.+)$/i, (_, name) => `Welcome back, ${name}`);
  result = result.replace(/^Asiento\s+#?(\d+)$/i, (_, id) => `Entry #${id}`);
  result = result.replace(/^Factura\s+(.+)$/i, (_, number) => `Invoice ${number}`);
  result = result.replace(/^Período:\s*(.+)$/i, (_, value) => `Period: ${value}`);

  return result;
};

const translateSpanishToEnglish = (text) => {
  if (!text || !text.trim()) return text;

  let result = applyRegexRules(text);

  for (const [spanish, english] of Object.entries(SPANISH_TO_ENGLISH_PHRASES)) {
    const pattern = new RegExp(escapeRegExp(spanish), 'gi');
    result = result.replace(pattern, (match) => applyCase(match, english));
  }

  result = result.replace(/[A-Za-zÁÉÍÓÚÜÑáéíóúüñ]+/g, (word) => {
    const translated = SPANISH_TO_ENGLISH_WORDS[word.toLowerCase()];
    return translated ? applyCase(word, translated) : word;
  });

  return result;
};

const shouldSkipNode = (node) => {
  if (!node) return true;
  const parent = node.parentElement;
  if (!parent) return true;
  if (SKIP_TAGS.has(parent.tagName)) return true;
  if (parent.isContentEditable) return true;
  if (parent.closest('[data-no-translate="true"]')) return true;
  if (parent.classList.contains('material-symbols-outlined')) return true;
  const value = node.nodeValue || '';
  return value.trim().length === 0;
};

export const createDomTranslator = (root, getLanguage) => {
  if (!root) {
    return {
      refresh: () => {},
      disconnect: () => {}
    };
  }

  const originalText = new WeakMap();
  const originalAttributes = new WeakMap();

  const translateByLanguage = (text, language) => {
    if (language === 'en') {
      return translateSpanishToEnglish(text);
    }
    return text;
  };

  const processTextNode = (textNode, language) => {
    if (shouldSkipNode(textNode)) return;

    if (!originalText.has(textNode)) {
      originalText.set(textNode, textNode.nodeValue ?? '');
    }

    const original = originalText.get(textNode) ?? '';
    const translated = translateByLanguage(original, language);
    if (textNode.nodeValue !== translated) {
      textNode.nodeValue = translated;
    }
  };

  const processElementAttributes = (element, language) => {
    if (!(element instanceof Element)) return;
    if (element.closest('[data-no-translate="true"]')) return;

    if (!originalAttributes.has(element)) {
      originalAttributes.set(element, {});
    }

    const storedAttributes = originalAttributes.get(element);

    for (const attr of TRANSLATABLE_ATTRIBUTES) {
      if (!element.hasAttribute(attr)) continue;

      if (!(attr in storedAttributes)) {
        storedAttributes[attr] = element.getAttribute(attr) ?? '';
      }

      const original = storedAttributes[attr];
      const translated = translateByLanguage(original, language);
      if (element.getAttribute(attr) !== translated) {
        element.setAttribute(attr, translated);
      }
    }
  };

  const processSubtree = (node, language) => {
    if (!node) return;

    if (node.nodeType === Node.TEXT_NODE) {
      processTextNode(node, language);
      return;
    }

    if (node.nodeType !== Node.ELEMENT_NODE) return;

    processElementAttributes(node, language);

    const textWalker = document.createTreeWalker(node, NodeFilter.SHOW_TEXT);
    let currentTextNode = textWalker.nextNode();
    while (currentTextNode) {
      processTextNode(currentTextNode, language);
      currentTextNode = textWalker.nextNode();
    }

    const elements = node.querySelectorAll('*');
    elements.forEach((element) => processElementAttributes(element, language));
  };

  const refresh = () => {
    processSubtree(root, getLanguage());
  };

  const observer = new MutationObserver((mutations) => {
    const language = getLanguage();
    for (const mutation of mutations) {
      if (mutation.type === 'characterData' && mutation.target?.nodeType === Node.TEXT_NODE) {
        processTextNode(mutation.target, language);
      }

      if (mutation.type === 'childList') {
        mutation.addedNodes.forEach((addedNode) => processSubtree(addedNode, language));
      }

      if (mutation.type === 'attributes' && mutation.target?.nodeType === Node.ELEMENT_NODE) {
        processElementAttributes(mutation.target, language);
      }
    }
  });

  observer.observe(root, {
    childList: true,
    subtree: true,
    characterData: true,
    attributes: true,
    attributeFilter: TRANSLATABLE_ATTRIBUTES
  });

  refresh();

  return {
    refresh,
    disconnect: () => observer.disconnect()
  };
};
